class Robot:
    population = 0

    def __init__(self, name, version):
        self.name = name
        self.version = version
        Robot.population += 1

    
    def say_hello(self):
        return f"Greetings. I am {self.name}, version {self.version}."

r1 = Robot("R2-D2", 1.0)
print(r1.say_hello())



